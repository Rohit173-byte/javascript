const month = "march" ;
switch (month) {
    case 1:
        console.log("jan");
        
        break;
    case 2:
        console.log("february");
        
        break;
    case 3:
        console.log("march");
        
        break;
    case 4:
        console.log("april");
        
        break;
    case 5:
        console.log("may");
        
        break;
    
    default:
        console.log("default");
        
        break;
}